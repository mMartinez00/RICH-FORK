function uniq(arr){
  return Array.from(new Set((arr || []).filter(Boolean)));
}

function cleanStr(s){
  return String(s || "").replace(/\s+/g, " ").trim();
}

function keyOfName(name){
  return cleanStr(name).toLowerCase();
}

function yearsFromSemester(s){
  s = cleanStr(s);
  if(!s) return [];

  // normalize en dash to hyphen for parsing
  const norm = s.replace(/–/g, "-");

  // grab all 4-digit years in the string
  const matches = norm.match(/\b(19|20)\d{2}\b/g) || [];
  const years = matches.map(x => parseInt(x, 10)).filter(n => !Number.isNaN(n));

  // If it's a range with 2 distinct years, include both (and any in between just in case)
  if(years.length >= 2){
    const minY = Math.min(...years);
    const maxY = Math.max(...years);
    const out = [];
    for(let y = minY; y <= maxY; y++) out.push(y);
    return out;
  }

  return years;
}


function displaySemesters(semesters){
  const list = (semesters || []).map(cleanStr).filter(Boolean);

  const parsed = list
    .map(s => {
      const m = s.match(/(\d{4})\s*[–-]\s*(\d{4})/);
      if(!m) return null;
      return { start: parseInt(m[1], 10), end: parseInt(m[2], 10) };
    })
    .filter(Boolean);

  
  if(parsed.length === list.length && parsed.length > 0){
    const minStart = Math.min(...parsed.map(x => x.start));
    const maxEnd = Math.max(...parsed.map(x => x.end));
    return `${minStart}–${maxEnd}`;
  }

  return list.join(", ");
}

function normalizeHS(h){
  if(!h) return null;
  return {
    name: cleanStr(h.name),
    img: cleanStr(h.img),
    bio: cleanStr(h.bio)
  };
}

function normalizeIntern(i){
  const hs = uniq((i.hsInterns || []).map(x => x))
    .map(normalizeHS)
    .filter(Boolean);

  return {
    name: cleanStr(i.name),
    role: cleanStr(i.role),
    img: cleanStr(i.img),
    tags: uniq((i.tags || []).map(cleanStr)).filter(Boolean),
    semesters: uniq((i.semesters || []).map(cleanStr)).filter(Boolean), 
    bio: cleanStr(i.bio),
    hsInterns: hs
  };
}


function mergeInterns(list){
  const map = new Map();

  for(const raw of (list || [])){
    const i = normalizeIntern(raw);
    if(!i.name) continue;

    const key = keyOfName(i.name);

    if(!map.has(key)){
      map.set(key, i);
      continue;
    }

    const existing = map.get(key);

    if(!existing.role && i.role) existing.role = i.role;
    if((!existing.img || existing.img === "") && i.img) existing.img = i.img;

    existing.tags = uniq([...(existing.tags||[]), ...(i.tags||[])]);
    existing.semesters = uniq([...(existing.semesters||[]), ...(i.semesters||[])]);

    
    const hsMap = new Map();
    for(const h of [...(existing.hsInterns||[]), ...(i.hsInterns||[])]){
      if(!h || !h.name) continue;
      hsMap.set(keyOfName(h.name), h);
    }
    existing.hsInterns = Array.from(hsMap.values());

    if((i.bio || "").length > (existing.bio || "").length){
      existing.bio = i.bio;
    }
  }

  return Array.from(map.values());
}

