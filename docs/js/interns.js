function wireHSReadMore(){
  if(!mHSGrid) return;

  mHSGrid.querySelectorAll(".hs-more").forEach(btn => {
    btn.addEventListener("click", (e) => {
      const card = e.target.closest(".hs-card");
      if(!card) return;

      card.classList.toggle("expanded");
      e.target.textContent = card.classList.contains("expanded") ? "Show less" : "Read more";
    });
  });
}

function openModal(i){
  if(!i) return;

  mImg.src = i.img;
  mImg.alt = i.name;
  mName.textContent = i.name;
  mRole.textContent = i.role;

  if(mSemesters){
    mSemesters.textContent = displaySemesters(i.semesters);
  }

  mBio.textContent = i.bio;

  
  const hs = Array.isArray(i.hsInterns) ? i.hsInterns : [];

  if(mHSWrap && mHSGrid){
    if(hs.length === 0){
      mHSWrap.style.display = "none";
      mHSGrid.innerHTML = "";
      mHSGrid.classList.remove("two-col");
    } else {
      mHSWrap.style.display = "block";
      mHSGrid.classList.toggle("two-col", hs.length >= 2);

      mHSGrid.innerHTML = hs.map(h => {
        const bio = h.bio || "";
        const showBtn = bio.length > 120;

        return `
          <article class="hs-card">
            <div class="hs-top">
              <img src="${h.img || ""}" alt="${h.name || "High School Intern"}">
              <div>
                <p class="hs-name">${h.name || ""}</p>
                <span class="hs-pill">High School Intern</span>
              </div>
            </div>
            <div class="hs-body">
              <p>${bio}</p>
              ${showBtn ? `<button class="hs-more" type="button">Read more</button>` : ``}
            </div>
          </article>
        `;
      }).join("");

      wireHSReadMore();
    }
  }

  modal.classList.add("open");
  modal.setAttribute("aria-hidden", "false");
  document.body.style.overflow = "hidden";
}

function closeModal(){
  modal.classList.remove("open");
  modal.setAttribute("aria-hidden", "true");
  document.body.style.overflow = "";
}

if(mCloseBtn) mCloseBtn.addEventListener("click", closeModal);

if(modal){
  modal.addEventListener("click", (e) => {
    if(e.target === modal) closeModal();
  });
}

window.addEventListener("keydown", (e) => {
  if(e.key === "Escape" && modal && modal.classList.contains("open")){
    closeModal();
  }
});

